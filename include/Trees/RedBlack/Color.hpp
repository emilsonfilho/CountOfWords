/**
 * @file Color.hpp
 * @brief Defines the Color enumeration used in Red-Black Tree nodes.
 *
 * This header file contains the definition of the `Color` enumeration,
 * which represents the color of a node in a Red-Black Tree. The two possible
 * values are `RED` and `BLACK`.
 *
 * @enum Color
 * @var Color::RED
 * Represents a red-colored node in the Red-Black Tree.
 * @var Color::BLACK
 * Represents a black-colored node in the Red-Black Tree.
 */
#ifndef COLOR_HPP
#define COLOR_HPP

enum Color { RED, BLACK };

#endif